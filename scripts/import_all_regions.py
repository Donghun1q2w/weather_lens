def import_to_db(regions: List[Dict]):
    """데이터베이스에 저장"""
    conn = sqlite3.connect(SQLITE_DB_PATH)
    cursor = conn.cursor()

    # 기존 데이터 삭제
    cursor.execute("DELETE FROM regions")

    # 새 데이터 삽입
    for r in regions:
        cursor.execute("""
            INSERT INTO regions
            (code, name, sido, sigungu, emd, lat, lon, nx, ny, elevation, is_coastal, is_east_coast, is_west_coast)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        """, (
            r["code"], r["name"], r["sido"], r["sigungu"], r["emd"],
            r["lat"], r["lon"], r["nx"], r["ny"], r["elevation"],
            r["is_coastal"], r["is_east_coast"], r["is_west_coast"]
        ))

    conn.commit()

    # 통계 출력
    cursor.execute("SELECT COUNT(*) FROM regions")
    total = cursor.fetchone()[0]

    cursor.execute("SELECT sido, COUNT(*) FROM regions GROUP BY sido ORDER BY COUNT(*) DESC")
    sido_stats = cursor.fetchall()

    cursor.execute("SELECT COUNT(*) FROM regions WHERE is_coastal = 1")
    coastal = cursor.fetchone()[0]

    cursor.execute("SELECT COUNT(*) FROM regions WHERE is_east_coast = 1")
    east = cursor.fetchone()[0]

    cursor.execute("SELECT COUNT(*) FROM regions WHERE is_west_coast = 1")
    west = cursor.fetchone()[0]

    cursor.execute("SELECT COUNT(*) FROM regions WHERE elevation >= 500")
    mountain = cursor.fetchone()[0]

    conn.close()

    print(f"\n{'='*60}")
    print("통계")
    print(f"{'='*60}")
    print(f"총 지역: {total}개")
    print(f"해안가: {coastal}개")
    print(f"동해안: {east}개")
    print(f"서해안: {west}개")
    print(f"산악 (500m+): {mountain}개")
    print(f"\n시도별:")
    for sido, count in sido_stats:
        print(f"  {sido}: {count}개")


