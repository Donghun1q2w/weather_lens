def import_regions_to_db(regions: List[Dict]):
    """
    지역 데이터를 SQLite DB에 삽입

    Args:
        regions: 지역 데이터 리스트
    """
    conn = sqlite3.connect(SQLITE_DB_PATH)
    cursor = conn.cursor()

    # 기존 데이터 삭제
    cursor.execute("DELETE FROM regions")

    # 데이터 삽입
    insert_sql = """
        INSERT INTO regions
        (code, name, sido, sigungu, emd, lat, lon, nx, ny, elevation, is_coastal, is_east_coast, ocean_station_id)
        VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
    """

    for region in regions:
        cursor.execute(insert_sql, (
            region["code"],
            region["name"],
            region["sido"],
            region["sigungu"],
            region["emd"],
            region["lat"],
            region["lon"],
            region["nx"],
            region["ny"],
            region["elevation"],
            region["is_coastal"],
            region["is_east_coast"],
            region["ocean_station_id"],
        ))

    conn.commit()

    # 통계 출력
    cursor.execute("SELECT COUNT(*) FROM regions")
    total = cursor.fetchone()[0]

    cursor.execute("SELECT sido, COUNT(*) FROM regions GROUP BY sido ORDER BY COUNT(*) DESC")
    sido_stats = cursor.fetchall()

    cursor.execute("SELECT COUNT(*) FROM regions WHERE is_coastal = 1")
    coastal_count = cursor.fetchone()[0]

    cursor.execute("SELECT COUNT(*) FROM regions WHERE is_east_coast = 1")
    east_coast_count = cursor.fetchone()[0]

    conn.close()

    print(f"\n📊 데이터베이스 통계:")
    print(f"   총 지역 수: {total}개")
    print(f"   해안가 지역: {coastal_count}개")
    print(f"   동해안 지역: {east_coast_count}개")
    print(f"\n   시도별 현황:")
    for sido, count in sido_stats:
        print(f"     - {sido}: {count}개")


