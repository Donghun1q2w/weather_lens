def get_beach_by_num(beach_num: int) -> Beach | None:
    """
    Get beach by beach number.

    Args:
        beach_num: Beach number (1-420)

    Returns:
        Beach data or None if not found
    """
    for beach in BEACHES:
        if beach["beach_num"] == beach_num:
            return beach
    return None


def get_beaches_by_region(region_code: str) -> List[Beach]:
    """
    Get all beaches in a specific region.

    Args:
        region_code: Region code (읍면동코드)

    Returns:
        List of beaches in the region
    """
    return [b for b in BEACHES if b["region_code"] == region_code]


def haversine_distance(lat1: float, lon1: float, lat2: float, lon2: float) -> float:
    """
    Calculate the great circle distance between two points on earth in kilometers.

    Args:
        lat1, lon1: First point coordinates
        lat2, lon2: Second point coordinates

    Returns:
        Distance in kilometers
    """
    # Convert decimal degrees to radians
    lon1, lat1, lon2, lat2 = map(radians, [lon1, lat1, lon2, lat2])

    # Haversine formula
    dlon = lon2 - lon1
    dlat = lat2 - lat1
    a = sin(dlat / 2) ** 2 + cos(lat1) * cos(lat2) * sin(dlon / 2) ** 2
    c = 2 * asin(sqrt(a))
    r = 6371  # Radius of earth in kilometers
    return c * r


def get_beaches_near_location(lat: float, lon: float, radius_km: float) -> List[Beach]:
    """
    Get all beaches within a specified radius of a location.

    Args:
        lat: Latitude of the center point
        lon: Longitude of the center point
        radius_km: Search radius in kilometers

    Returns:
        List of beaches within the radius, sorted by distance
    """
    nearby_beaches = []
    for beach in BEACHES:
        distance = haversine_distance(lat, lon, beach["lat"], beach["lon"])
        if distance <= radius_km:
            nearby_beaches.append((distance, beach))

    # Sort by distance and return beaches only
    nearby_beaches.sort(key=lambda x: x[0])
    return [beach for _, beach in nearby_beaches]
